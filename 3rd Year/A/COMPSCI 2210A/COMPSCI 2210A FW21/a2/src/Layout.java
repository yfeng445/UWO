
public class Layout {
	
	private String boardLayout;
	private int score;
	
	public Layout(String boardLayout, int score) {
		this.boardLayout = boardLayout;
		this.score = score;
	}
	
	public String getBoardLayout() {
		return boardLayout;
	}
	
	public int getScore() {
		return score;
	}
	
}
