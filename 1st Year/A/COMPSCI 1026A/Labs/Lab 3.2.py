month = str(input("Please enter the month of the year: "))
if month=="December" or month == "January" or month == "February":
    print("In ",month,",it is winter, I suggest you go snowboarding.")
elif month=="March" or month =="April" or month =="May":
    print ("In ", month, ",it is spring, I suggest you go hiking.")
elif month=="June" or month =="July" or month =="August":
    print("In ", month, ",it is summer, I suggest you go swimming.")
elif month=="September" or month =="October" or month =="November":
    print("In ", month, ",it is autumn, I suggest you go biking.")
